   public void atualizarTotais() {
    ReceitaController receitaCtrl = new ReceitaController();
    DespesaController despesaCtrl = new DespesaController();

    double totalReceitas = receitaCtrl.listarReceitas()
                                      .stream()
                                      .mapToDouble(r -> r.getValor())
                                      .sum();

    double totalDespesas = despesaCtrl.listarDespesas()
                                      .stream()
                                      .mapToDouble(d -> d.getValor())
                                      .sum();

    double saldo = totalReceitas - totalDespesas;

    jTextPane1.setText(String.format("%.2f", totalDespesas)); // Despesas
    jTextPane2.setText(String.format("%.2f", totalReceitas)); // Receitas
    jTextPane3.setText(String.format("%.2f", saldo));         // Saldo
}
